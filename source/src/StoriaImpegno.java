  private void PAN_PARAMETRI_InitQueries()
  {
    StringBuffer SQL;

    PAN_PARAMETRI.SetSize(MyGlb.OBJ_QUERY, 2);
    SQL = new StringBuffer();
    SQL.append("select ");
    SQL.append("  A.DESCRIZIONE as IMPDESCRIZIO ");
    SQL.append("from ");
    SQL.append("  IMP A ");
    SQL.append("where (A.ANNO_IMP = ~~NOMOGGANNIMP~~) ");
    SQL.append("and   (A.NUMERO_IMP = ~~NOMEOGGEIMPE~~) ");
    PAN_PARAMETRI.SetQuery(PPQRY_IMP1, 0, SQL, -1, "");
    PAN_PARAMETRI.SetQueryDB(PPQRY_IMP1, MainFrm.Cf4armDBObject.DB, 4);
    PAN_PARAMETRI.SetMasterTable(PPQRY_IMP1, "IMP");
    PAN_PARAMETRI.SetIMDB(IMDB, "PQRY_NUOVATABELL3", true);
    PAN_PARAMETRI.set_SetString(MyGlb.MASTER_ROWNAME, "Nome Oggetto");
    PAN_PARAMETRI.SetQueryIMDB(PPQRY_NUOVATABELL3, IMDBDef13.PQRY_NUOVATABELL3_RS, IMDBDef4.TBL_NUOVATABELL4);
    JustLoaded = true;
    PAN_PARAMETRI.SetFieldPrimaryIndex(PFL_PARAMETRI_IMPEGNO, IMDBDef4.FLD_NUOVATABELL4_NOMEOGGEIMPE);
    PAN_PARAMETRI.SetFieldPrimaryIndex(PFL_PARAMETRI_ANNOIMPEGNO, IMDBDef4.FLD_NUOVATABELL4_NOMOGGANNIMP);
    PAN_PARAMETRI.SetMasterTable(0, "NUOVATABELL4");
    //
    // Rieseguo la query se il pannello era gi in stato DATA
    if (PAN_PARAMETRI.Status() == 2)
    {
      int oldListQBE = PAN_PARAMETRI.iUseListQBE;
      PAN_PARAMETRI.iUseListQBE = 0;
      PAN_PARAMETRI.PanelCommand(Glb.PCM_SEARCH);
      PAN_PARAMETRI.PanelCommand(Glb.PCM_FIND);
      PAN_PARAMETRI.iUseListQBE = oldListQBE;
    }
  }

  
  
