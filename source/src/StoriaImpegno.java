  // **********************************************************************
  // Parametri On Updating Row
  // Evento notificato dal pannello quando un utente modifica
  // i dati presenti nel pannello
  // Column - Input
  // Field Modified - Input
  // Field Was Modified - Input
  // Row Was Modified - Input
  // Inserting - Input
  // Cancel - Input/Output
  // **********************************************************************
  private void PAN_PARAMETRI_OnUpdatingRow(IDVariant Column, IDVariant FieldModified, IDVariant FieldWasModified, IDVariant RowWasModified, IDVariant Inserting, IDVariant Cancel)
  {
    StringBuffer SQL = new StringBuffer();
    int TransCount   = 0;
    int ReturnStatus = 0;
    IDCachedRowSet QV;

    try
    {
      TransCount = 0;
      // 
      // Parametri On Updating Row Body
      // Corpo Procedura
      // 
      if ((Column.equals((new IDVariant(PFL_PARAMETRI_IMPEGNO)), true) || Column.equals((new IDVariant(PFL_PARAMETRI_ANNOIMPEGNO)), true)) && FieldWasModified.booleanValue())
      {
        if (!(IDL.IsNull(IMDB.Value(IMDBDef13.PQRY_NUOVATABELL3, IMDBDef13.PQSL_NUOVATABELL3_NOMOGGANNIMP, 0))) && !(IDL.IsNull(IMDB.Value(IMDBDef13.PQRY_NUOVATABELL3, IMDBDef13.PQSL_NUOVATABELL3_NOMEOGGEIMPE, 0))))
        {
          IDVariant v_VCOUNT = new IDVariant(0,IDVariant.INTEGER);
          SQL = new StringBuffer();
          SQL.append("select ");
          SQL.append("  COUNT(*) as COUNT1 ");
          SQL.append("from ");
          SQL.append("  IMP A ");
          SQL.append("where (A.ANNO_IMP = " + IDL.CSql(IMDB.Value(IMDBDef13.PQRY_NUOVATABELL3, IMDBDef13.PQSL_NUOVATABELL3_NOMOGGANNIMP, 0), IDL.FMT_NUM, MainFrm.Cf4armDBObject.DBO()) + ") ");
          SQL.append("and   (A.NUMERO_IMP = " + IDL.CSql(IMDB.Value(IMDBDef13.PQRY_NUOVATABELL3, IMDBDef13.PQSL_NUOVATABELL3_NOMEOGGEIMPE, 0), IDL.FMT_NUM, MainFrm.Cf4armDBObject.DBO()) + ") ");
          QV = MainFrm.Cf4armDBObject.DBO().OpenRS(SQL);
          if (!QV.EOF()) QV.MoveNext();
          if (!QV.EOF())
          {
            v_VCOUNT = QV.Get("COUNT1", IDVariant.INTEGER) ;
          }
          QV.Close();
          if (v_VCOUNT.equals((new IDVariant(0)), true))
          {
            IDVariant S = new IDVariant(0,IDVariant.STRING);
            S = (new IDVariant("Impegno inesistente"));
            MainFrm.set_AlertMessage(S); 
            IMDB.set_Value(IMDBDef13.PQRY_NUOVATABELL3, IMDBDef13.PQSL_NUOVATABELL3_NOMEOGGEIMPE, 0, (new IDVariant()));
            IMDB.set_Value(IMDBDef13.PQRY_NUOVATABELL3, IMDBDef13.PQSL_NUOVATABELL3_NOMOGGANNIMP, 0, (new IDVariant()));
          }
        }
      }
    }
    catch (Exception _e)
    {
      MainFrm.ErrObj.ProcError ("StoriaImpegno", "ParametriOnUpdatingRow", _e);
    }
  }

