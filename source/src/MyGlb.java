  public static void ShiftFrmConst(int frmOfs, WebEntryPoint w)
  {
    if (MyGlb.FRM_OFFSET == 0)
    {
      MyGlb.FRM_OFFSET += frmOfs;
      ShiftStaticConst(w);
    }
  }
  
  public static void ShiftStaticConst(WebEntryPoint MainFrm)
  {
    if (MyGlb.FRM_OFFSET != 0)
    {
      try
      {
        String tn = (Glb.ClassWithPackage(MainFrm.getClass()) ? MainFrm.getClass().getPackage().getName() + "." : "");
        Field[] props = Class.forName(tn + "MyGlb").getFields();
        for (int j = 0; j < props.length; j++)
        {
          Field p = props[j];
          if (p.getName().startsWith("FRM_"))
            p.setInt(null, p.getInt(null) + MyGlb.FRM_OFFSET);
        }
      }
      catch (Exception e)
      {
        e.printStackTrace(System.err);
      }
    }
  }
  
