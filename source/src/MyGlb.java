  public static void readObject(WebEntryPoint MainFrm, ObjectInputStream in) throws Exception
  {
    // Leggo propriet statiche
    // Se l'offset era gi impostato devo prima de-offsettare tutte le define statiche prima di poter caricare il nuovo contesto statico
    if (MyGlb.FRM_OFFSET != 0)
    {
      MyGlb.FRM_OFFSET = -MyGlb.FRM_OFFSET;
      ShiftStaticConst(MainFrm);
    }
    //
    MyGlb.FRM_OFFSET = in.readInt();
    ShiftStaticConst(MainFrm);
  }
  public static void writeObject(WebEntryPoint MainFrm, ObjectOutputStream out) throws Exception
  {
    // Scrivo propriet statiche
    out.writeInt(MyGlb.FRM_OFFSET);
  }
