        {
          v_PRESPAZIO = IDL.SubStr(v_CAPTIONCS, IDL.Sub(I, (new IDVariant(1))), (new IDVariant(1)));
        }
        else
        {
          v_PRESPAZIO = IDL.Right(v_CAPTIONCS, (new IDVariant(1)));
        }
