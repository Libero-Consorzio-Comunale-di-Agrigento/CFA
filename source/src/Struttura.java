        IDVariant I = new IDVariant(0,IDVariant.INTEGER);
        // 
        // ----------- Calcolo maschile o femminile per Nuovo
        // o Nuova
        // 
        v_CAPTIONCS = GetInformazioni();
        I = IDL.Find(v_CAPTIONCS, (new IDVariant(" ")));
